signature X86ICODETRANSFORMSIG =
sig
    type 'reg x86ICode and reg and abstract and 'reg basicBlock
    type address = Address.address

    val codeICodeFunctionToX86: {blocks: abstract basicBlock vector, functionName: string, maxLabels: int, maxPRegs: int,
            argRegsUsed: reg list, hasFullClosure: bool, currentStackArgs: int,
            debugSwitches: Universal.universal list } -> address

    structure Sharing:
    sig
        type 'reg x86ICode  = 'reg x86ICode
        and abstract        = abstract
        and reg             = reg
        and 'reg basicBlock = 'reg basicBlock
    end
end;
